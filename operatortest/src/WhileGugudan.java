public class WhileGugudan 
{
	public static void main(String[] args) 
	{
		System.out.println("**备备窜1**");
		int dan=2;
		 while(dan<10){
			 int i=1;
            while(i<10){
				System.out.print(dan + " * " + i + " = " + dan*i + "  ");
				i++;
            }
        dan++;
        System.out.println();
		 }

		System.out.println("**备备窜2**");
		int dan2=1;
		 while(dan2<10){
	 		int i2=2;
            while(i2<10){
				System.out.print(i2 + " * " + dan2 + " = " + dan2*i2 + "  ");	
				i2++;
            }
        dan2++;
        System.out.println();
		 }


	}
}
